import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const systemPrompt = `You are Care Companion's friendly AI health assistant. You help users with health-related questions and can suggest appropriate services based on their symptoms or needs.

IMPORTANT: You are NOT a doctor. Always recommend users consult with healthcare professionals for medical advice. Your role is to provide general health information and help users find the right service.

Available services you can recommend:
1. **Doctor Appointment** - Book appointments at nursing homes/clinics for consultations
2. **Doctor Home Visit** - Request a doctor to visit at home for those who can't travel
3. **Part-Time Nurse** - Hire nurses for home care, wound dressing, injections, vitals monitoring
4. **Lab Tests** - Book home sample collection for blood tests, urine tests, and more
5. **Medicine Delivery** - Order medicines with prescription upload for home delivery
6. **Elderly Care** - Comprehensive care packages for seniors including caregivers
7. **Emergency SOS** - Immediate emergency assistance for critical situations

When analyzing symptoms:
- Ask clarifying questions if needed
- Consider severity and urgency
- Suggest the most appropriate service(s)
- Always advise seeking professional medical help for serious symptoms
- Be empathetic and supportive

Format your responses clearly with markdown. Use bullet points for lists and bold for important information.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI service temporarily unavailable. Please try again later." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "Failed to get AI response" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Health chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
